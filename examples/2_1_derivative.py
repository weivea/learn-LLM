"""
2.1 导数(derivative) —— 验证脚本

配套知识点：数学知识点掌握表.md · 2.1
目标：用代码把「导数 = 变化率 = 切线斜率」跑一遍，看清：
      1) 割线随 h→0 逼近切线斜率
      2) 数值导数(差商) ≈ 解析导数
      3) 中心差分比前向差分更准
      4) 导数正负 = 上升/下降；一阶近似 = 参数变一点损失变多少
      5) 顺着导数反方向走一步，函数值真的变小(梯度下降预演)

运行：
    python examples/2_1_derivative.py
"""

import numpy as np


def section(title: str) -> None:
    print("\n" + "=" * 58)
    print(title)
    print("=" * 58)


def f(x):
    """示例函数 f(x) = x^2。"""
    return x ** 2


def df(x):
    """解析导数 f'(x) = 2x（幂法则 x^n -> n·x^(n-1)）。"""
    return 2 * x


def forward_diff(func, x, h):
    """前向差分：(f(x+h) - f(x)) / h。"""
    return (func(x + h) - func(x)) / h


def central_diff(func, x, h):
    """中心差分：(f(x+h) - f(x-h)) / (2h)，误差更小。"""
    return (func(x + h) - func(x - h)) / (2 * h)


def main() -> None:
    x0 = 3.0  # 我们全程盯着 f(x)=x^2 在 x=3 这一点，真值 f'(3)=2*3=6

    # ---------------------------------------------------------------
    section("1) 割线随 h→0 逼近切线斜率（真值 f'(3)=6）")
    print("f(x)=x^2, 在 x=3 处看『两点割线斜率』如何逼近导数：")
    for h in [1.0, 0.5, 0.1, 0.01, 0.001, 0.0001]:
        slope = forward_diff(f, x0, h)
        print(f"  h={h:<8} 割线斜率=(f(3+h)-f(3))/h = {slope:.6f}")
    print("=> h 越小越贴近 6，这就是极限 h→0 时割线变切线")

    # ---------------------------------------------------------------
    section("2) 数值导数 ≈ 解析导数（差商 vs 2x）")
    h = 1e-5
    for x in [-2.0, 0.0, 1.0, 3.0, 5.0]:
        num = central_diff(f, x, h)
        ana = df(x)
        print(f"  x={x:>4}: 数值={num:>9.5f}  解析 2x={ana:>5.1f}  差={abs(num-ana):.2e}")
    print("=> 不用手推公式，只要能算 f，就能把导数估出来（框架做梯度检查就靠它）")

    # ---------------------------------------------------------------
    section("3) 中心差分比前向差分更准")
    h = 1e-3
    fwd = forward_diff(f, x0, h)
    cen = central_diff(f, x0, h)
    print(f"真值 f'(3) = 6")
    print(f"  前向差分 (h={h}) = {fwd:.6f}   误差 {abs(fwd-6):.2e}")
    print(f"  中心差分 (h={h}) = {cen:.6f}   误差 {abs(cen-6):.2e}  <- 明显更小")
    print("=> 同样的 h，中心差分两边各走一步，误差小一个量级，推荐用它")

    # ---------------------------------------------------------------
    section("4) 导数的正负 = 函数在上升还是下降")
    print("f(x)=x^2 是抛物线，顶点在 0。看不同点的导数符号：")
    for x in [-3.0, -1.0, 0.0, 1.0, 3.0]:
        d = df(x)
        if d > 0:
            state = "斜率>0 → 上升"
        elif d < 0:
            state = "斜率<0 → 下降"
        else:
            state = "斜率=0 → 驻点(谷底)"
        print(f"  x={x:>4}: f'={d:>5.1f}   {state}")
    print("=> 正负决定『往哪调』，绝对值大小决定『这一步影响多大』")

    # ---------------------------------------------------------------
    section("5) 一阶近似：Δy ≈ f'(x)·Δx（参数变一点，输出变多少）")
    x = 3.0
    slope = df(x)  # = 6
    print(f"在 x=3, f'(3)={slope}. 预测 f(3+Δx) ≈ f(3) + 6·Δx：")
    for dx in [0.1, 0.01, -0.05]:
        predict = f(x) + slope * dx     # 一阶近似
        true = f(x + dx)                # 真实值
        print(f"  Δx={dx:>6}: 预测Δy={slope*dx:>8.4f}  真实Δy={true-f(x):>8.4f}  近似值={predict:.4f} 真值={true:.4f}")
    print("=> Δx 越小，一阶近似越准。这就是『w 变一点，损失 L 变多少』的原型")

    # ---------------------------------------------------------------
    section("6) 梯度下降预演：顺着导数反方向走一步，f 变小")
    x = 3.0
    eta = 0.1  # 学习率(步长)
    print(f"起点 x={x}, f(x)={f(x)}, 目标：让 f=x^2 变小（最小在 x=0）")
    for step in range(1, 6):
        g = df(x)              # 当前点导数
        x = x - eta * g        # 关键：减去 η·导数，反着斜率走
        print(f"  第{step}步: 导数={g:>7.4f}  更新后 x={x:.5f}  f(x)={f(x):.5f}")
    print("=> 每步都朝导数反方向挪 η 步长，f 一路下降、x 逼近谷底 0")
    print("   这正是 2.5 梯度下降 w ← w - η·(dL/dw) 的一维版本")

    print("\n✅ 全部跑通！导数 = 变化率 = 切线斜率：正负指方向，大小指影响，是训练的方向盘。")


if __name__ == "__main__":
    main()
